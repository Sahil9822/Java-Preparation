public class ShortHandOperatorTricks 
{
	public static void main(String[] args) 
	{
		int i = 5;
		int j = 10;
		int k = 15;
		int l = 20;
		i += 2;
		j -= 2;
		k *= 2;
		l /= 2;
		System.out.println(i);
		System.out.println(j);
		System.out.println(k);
		System.out.println(l);
	}
}
